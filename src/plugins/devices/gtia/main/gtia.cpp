bool GTIA::ioRead(IBus* bus, uint32_t addr, uint8_t* val) {
    if ((addr & ~addrMask()) != baseAddr()) return false;
    uint32_t offset = addr & 0x001F;
    if (offset < 0x10) {
        *val = m_collisions[offset];
        return true;
    }
    switch (offset) {
        case TRIG0:
        case TRIG1:
        case TRIG2:
        case TRIG3:
            *val = 0x01; // Not pressed (simplified)
            return true;
        case PAL:
            *val = m_isPal ? 0x01 : 0x0F; 
            return true;
        case CONSOL:
            *val = m_consoleSwitches;
            return true;
        default:
            // Allow reading write-only registers for testing/debugging
            *val = m_regs[offset];
            return true;
    }
}

bool GTIA::ioWrite(IBus* bus, uint32_t addr, uint8_t val) {
    if ((addr & ~addrMask()) != baseAddr()) return false;
    uint32_t offset = addr & 0x001F;
    m_regs[offset] = val;

    if (offset == HITCLR) {
        std::memset(m_collisions, 0, sizeof(m_collisions));
    }
    
    return true;
}

void GTIA::reset() {
    std::memset(m_regs, 0, sizeof(m_regs));
    std::memset(m_collisions, 0, sizeof(m_collisions));
    m_consoleSwitches = 0x07;
}

